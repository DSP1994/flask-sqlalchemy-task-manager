from taskmanager import db


class Category(db.Model):
    # schema for the Category model
    id = db.Column(db.Integer, primary_key=True)
    category_name = db.Column(db.String(25), unique=True, nullable=False)
    # nullable means it's a required field
    tasks = db.relationship("Task", backref="category", cascade="all, delete",
                            lazy=True)
    # category.id has been pulled from the 'Category' class above 'id = ...'

    def __repr__(self):
        # __repr__ to represent itself in the form of a string
        return self.category_name


class Task(db.Model):
    # schema for the Task model
    id = db.Column(db.Integer, primary_key=True)
    task_name = db.Column(db.String(50), unique=True, nullable=False)
    task_description = db.Column(db.Text, nullable=False)
    is_urgent = db.Column(db.Boolean, default=False, nullable=False)
    due_date = db.Column(db.Date, nullable=False)
    category_id = db.Column(db.Integer, db.ForeignKey("category.id",
                            ondelete="CASCADE"), nullable=False)
    # CASCADE is needed as each task needs a category, you can have many
    # tasks per category but not many category's per task.
    # If a category is deltd it will also delete the tasks assosiated with it.
    def __repr__(self):
        # __repr__ to represent itself in the form of a string
        return "#{0} - Task: {1} | Urgent: {2}".format(
            self.id, self.task_name, self.is_urgent
        )
